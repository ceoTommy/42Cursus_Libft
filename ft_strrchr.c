#include "libft.h"
#include <stdlib.h>

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i)
	{
		if (s[i] == (char)c)
			return (((char *)s) + i);
		i--;
	}
	if (s[0] == (char)c)
		return (((char *) s) + i);
	return (NULL);
}
